	    public class Max {

        /**
         *  Метод возвращает максимальное число. 
         *
         * @param first первое число.
		 * @param second второе число.
         * @return максимальное число.
         */
            public int max(int first, int second) {

            return first > second ? first : second;
        }
    }
	
	
	
	